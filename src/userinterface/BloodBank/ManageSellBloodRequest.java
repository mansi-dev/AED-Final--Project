    private void populateRequestTable() {
        DefaultTableModel model = (DefaultTableModel) donorTrnTable.getModel();
        model.setRowCount(0);
        for (Network n : this.ecosystem.getNetworkList()) {
            for (Enterprise e : n.getEnterpriseDirectory().getEnterpriseList()) {
                if (e instanceof BloodBankEnterprise) {
                    for (Organizations org : e.getOrganizationDirectory().getOrganizationList()) {
                        if (org instanceof BloodBankOrganization) {
                            DefaultTableModel modelR = (DefaultTableModel) donorTrnTable.getModel();
                            modelR.setRowCount(0);
                            for (WorkRequest request : org.getWorkQueue().getWorkRequestList()) {
                                if (request instanceof OrderBloodSampleWorkRequest) {
                                    int quant = ((OrderBloodSampleWorkRequest) request).getQuantity();

                                    Object[] rowTrn = new Object[13];
                                    //row[0] = ++index;

                                    rowTrn[0] = request;
                                    rowTrn[1] = ((OrderBloodSampleWorkRequest) request).getName();
                                    rowTrn[2] = ((OrderBloodSampleWorkRequest) request).getBloodGroup();
                                    rowTrn[3] = ((OrderBloodSampleWorkRequest) request).getQuantity();
                                    rowTrn[4] = quant * 300;
                                    rowTrn[5] = request.getStatus();
                                    modelR.addRow(rowTrn);
                                }
                            }
                            break;
                        }
                    }
                    break;
                }
            }
        }
    }

    private void updateHospitalInventory(String bloodGroup, int quantityReq, int hospitalID) {
        Organizations org = null;
        ArrayList<Enterprise> enterpriseList = EcoSystem.getInstance().getNetworkList().get(0).getEnterpriseDirectory().getEnterpriseList();
        Enterprise enterpriseTemp = enterpriseList.stream().filter(item -> "Hospital".equals(item.getName())).findFirst().orElse(null);

        for (Organizations organization : enterpriseTemp.getOrganizationDirectory().getOrganizationList()) {
            if (organization.getName().equals("Hospital Organization")) {

                System.out.println(organization);
                org = organization;
                break;
            }
        }

        if (org != null) {
            HospitalOrganization hOrg = (HospitalOrganization) org;
            Hospital hospitalByID = hOrg.getHospitalDirectory().getHospitalByID(hospitalID);
            BloodStock obj = hospitalByID.getBloodStock().stream().filter(item -> bloodGroup.equals(item.getBloodGroup())).findFirst().orElse(null);
            int quant;
            if (obj != null) {
                quant = obj.getQuantity() + quantityReq;
                obj.setQuantity(quant);

            } else {
                quant = quantityReq;
                List<BloodStock> bs = new ArrayList<>();
                bs.add(new BloodStock(bloodGroup, quant));
                hospitalByID.setBloodStock(bs);

            }
        }
    }
