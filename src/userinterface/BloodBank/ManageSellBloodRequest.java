    private void btnApproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApproveActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) donorTrnTable.getModel();
        int selectedRow = donorTrnTable.getSelectedRow();
        if (selectedRow != -1) {
            OrderBloodSampleWorkRequest db = (OrderBloodSampleWorkRequest) model.getValueAt(selectedRow, 0);
            if (db.getStatus().equals("Approved")) {
                JOptionPane.showMessageDialog(this, "Request already approved");
            } else {
                BloodBankManager user = (BloodBankManager) account.getUser();
                BloodStock bloodStock = user.getBloodBank().getBloodStock().stream().filter(item -> db.getBloodGroup().equals(item.getBloodGroup())).findFirst().orElse(null);
                int quantityAvailable = bloodStock.getQuantity();
                int quantityReq = db.getQuantity();
                if (bloodStock != null && quantityReq <= quantityAvailable) {
                    db.setStatus("Approved");
                    db.setResolveDate(new Date());
                    db.setReceiver(account);
                    int id = db.getHospitalID();

                    updateHospitalInventory(db.getBloodGroup(), quantityReq, id);

                    bloodStock.setQuantity(quantityAvailable - quantityReq);

                } else {
                    db.setStatus("Rejected");
                    db.setResolveDate(new Date());
                    db.setReceiver(account);
                    db.setMessage("Out of stock");
                }

                populateRequestTable();
            }

        } else {
            JOptionPane.showMessageDialog(this, "Please select a record to approve the request");

        }
    }//GEN-LAST:event_btnApproveActionPerformed


